# 1. Arithmetic Operators:
# +, -, *, /, %, //, **

a = 10
b = 3
print("a + b =", a + b)  # Addition
print("a - b =", a - b)  # Subtraction
print("a * b =", a * b)  # Multiplication

# Division => it gives us float value by using division operator
# => it gives us quotient value
print("a / b =", a / b)  # Division

## Floor Division => it gives us quotient value by removing decimal part
# => it gives us integer value
print("a // b =", a // b)  # Floor Division


# Modulus ("%") => it gives us remainder value
print("a % b =", a % b)  # Modulus

# Exponentiation Operator (**) => it gives us power value

print(10 ** 3)  # 10^3 = 10*10*10 = 1000
